// – У змінній day дано якесь число від 1 до 31. Потрібно визначити, у яку половину (декаду) місяця потрапляє це число (у першу, другу чи третю).

let day = +prompt('Введіть число від 1 до 31');
if (0<day && day<=10) console.log('перша частина')
else if (10<day && day<=20) console.log('друга частина')
else if (20<day && day<=31) console.log('третя частина')
else console.log('не вірне значення')